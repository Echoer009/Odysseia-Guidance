export function buildTourQueue(selectedTags: string[]): TourSlide[] {
  const channelIdSet = new Set<string>()

  for (const tagName of selectedTags) {
    const tag = tagsConfig.find(t => t.name === tagName)
    if (tag) {
      for (const id of tag.channelIds) {
        channelIdSet.add(id)
      }
    }
  }

  const slides: TourSlide[] = []
  for (const channelId of channelIdSet) {
    const name = channelNameMap[channelId] || '未知频道'
    const desc = channelDescMap[channelId] || '这是一个有趣的频道。'
    const type = channelTypeMap[channelId] || 'channel'
    const footer = channelFooterMap[channelId] || '类脑社区'

    slides.push({
      channelId,
      channelName: name,
      channelType: type,
      title: name,
      description: desc,
      footer,
      thumbnailUrl: `/assets/channels/${channelId}.webp`,
    })
  }

  return slides
}

export function getAllChannelIds(): string[] {
  const ids = new Set<string>()
  for (const tag of tagsConfig) {
    for (const id of tag.channelIds) {
      ids.add(id)
    }
  }
  return Array.from(ids)
}
