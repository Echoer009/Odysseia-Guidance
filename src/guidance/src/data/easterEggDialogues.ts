export function getIdleDialogue(count: number): string {
  if (count <= 1) {
    return idleDialogues[Math.floor(Math.random() * idleDialogues.length)]
  }
  return idleFollowUp[Math.floor(Math.random() * idleFollowUp.length)]
}

export function getDragDialogue(phase: number): string {
  const pool = dragDialogues[phase]
  if (!pool || pool.length === 0) return '……'
  return pool[Math.floor(Math.random() * pool.length)]
}

export function getSkipRageDialogue(): string {
  return skipRageDialogues[Math.floor(Math.random() * skipRageDialogues.length)]
}
